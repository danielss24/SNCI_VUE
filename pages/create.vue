<template>
  <v-container class="fill-height container" fluid>
    <video style="position:fixed; left: 0px;" playsinline="playsinline" autoplay="autoplay" muted="muted" loop="loop">
      <source src="/create.mp4" type="video/mp4">
    </video>
    <v-row>
      <v-card width="400" class="mx-auto mt-5" tile>
        <v-card-title>
          <h1 class="display-1">
            Nueva cuenta
          </h1>
        </v-card-title>
        <v-card-text>
          <v-form>
            <v-text-field v-model="email" label="Email" prepend-icon="mdi-account-circle" />
            <v-text-field
              v-model="password"
              :type="showPassword ? 'text' : 'password'"
              label="password"
              prepend-icon="mdi-lock"
              :append-icon="showPassword ? 'mdi-eye' : 'mdi-eye-off'"
              @click:append="showPassword = !showPassword"
            />
          </v-form>
        </v-card-text>
        <v-divider />
        <v-card-actions>
          <v-btn color="success" to="/login">
            Login
          </v-btn>
          <v-spacer />
          <v-btn color="info" @click="create">
            Create
          </v-btn>
        </v-card-actions>
      </v-card>
    </v-row>
    <!-- <v-row>
      <nuxt-link class="text-center" to="/recovery">
        recovery passoword
      </nuxt-link>
    </v-row> -->
  </v-container>
</template>

<script>

import firebase from "firebase/app";
import 'firebase/auth';
export default {
data: () => ({
    email: '',
    password: '',
    showPassword: false,
    errors: ''
}),
computed:{
    email(){
        if (this.$fireAuth.currentUser) {
            return this.$fireAuth.currentUser.email
        }
    }
},
methods: {
    create () {
        firebase.auth().createUserWithEmailAndPassword(this.email,this.password).then(user => {
            console.log(user);
            this.$router.push('/profile')
        })
        .catch((error) => {
            this.errors = error;
            alert(error.message)
        })
    }
}
}
</script>

